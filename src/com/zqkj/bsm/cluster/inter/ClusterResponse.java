package com.zqkj.bsm.cluster.inter;

public class ClusterResponse {
	private int responseCode;
	private String res_detail;
	private int subResponseCode = -1;//自定义子错误码
	private String resultBody;
	private String cookie;
	private String Content_Length="0";
	private String Content_Type="image/jpeg";
	private byte[] body_by;
	public byte[] getBody_by() {
		return body_by;
	}
	public void setBody_by(byte[] body_by) {
		this.body_by = body_by;
	}
	public String getCookie() {
		return cookie;
	}
	public void setCookie(String cookie) {
		this.cookie = cookie;
	}
	public int getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(int responseCode) {
		this.responseCode = responseCode;
	}
	public String getResultBody() {
		return resultBody;
	}
	public void setResultBody(String resultBody) {
		this.resultBody = resultBody;
	}
	public int getSubResponseCode() {
		return subResponseCode;
	}
	public void setSubResponseCode(int subResponseCode) {
		this.subResponseCode = subResponseCode;
	}
	public String getRes_detail() {
		return res_detail;
	}
	public void setRes_detail(String res_detail) {
		this.res_detail = res_detail;
	}
	public String getContent_Length() {
		return Content_Length;
	}
	public void setContent_Length(String content_Length) {
		Content_Length = content_Length;
	}
	public String getContent_Type() {
		return Content_Type;
	}
	public void setContent_Type(String content_Type) {
		Content_Type = content_Type;
	}
}
