package com.zqkj.bsm.dal.bean;

public class Plans {
	private  long plansId; 
	private String plansName; 
	private String plansAmount;
	private String plansType;
	private String node;
	private String weight;
	private String bjCounts;
	private String bjDays;
	private String skyDrive;
	private String status;
	private String qidType;
	
	
	public String getQidType() {
		return qidType;
	}
	public void setQidType(String qidType) {
		this.qidType = qidType;
	}
	public long getPlansId() {
		return plansId;
	}
	public void setPlansId(long plansId) {
		this.plansId = plansId;
	}
	public String getPlansName() {
		return plansName;
	}
	public void setPlansName(String plansName) {
		this.plansName = plansName;
	}
	public String getPlansAmount() {
		return plansAmount;
	}
	public void setPlansAmount(String plansAmount) {
		this.plansAmount = plansAmount;
	}
	public String getPlansType() {
		return plansType;
	}
	public void setPlansType(String plansType) {
		this.plansType = plansType;
	}
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	
	public String getBjCounts() {
		return bjCounts;
	}
	public void setBjCounts(String bjCounts) {
		this.bjCounts = bjCounts;
	}
	public String getBjDays() {
		return bjDays;
	}
	public void setBjDays(String bjDays) {
		this.bjDays = bjDays;
	}
	public String getSkyDrive() {
		return skyDrive;
	}
	public void setSkyDrive(String skyDrive) {
		this.skyDrive = skyDrive;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
